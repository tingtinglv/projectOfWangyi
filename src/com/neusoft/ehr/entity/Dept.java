package com.neusoft.ehr.entity;


public class Dept {
    private int dep_id;
	
	private String dep_seq;
    private String dep_name;
    private String dep_type;
    private String dep_tel;
    private String dep_fax;
    private String dep_desc;
    private String dep_updep;
    private String dep_date;
    
    public int getDep_id() {
		return dep_id;
	}
	public void setDep_id(int depId) {
		dep_id = depId;
	}
	public String getDep_seq() {
		return dep_seq;
	}
	public void setDep_seq(String depseq) {
		dep_seq = depseq;
	}
	public String getDep_name() {
		return dep_name;
	}
	public void setDep_name(String depName) {
		dep_name = depName;
	}
	public String getDep_type() {
		return dep_type;
	}
	public void setDep_type(String depType) {
		dep_type = depType;
	}
	public String getDep_tel() {
		return dep_tel;
	}
	public void setDep_tel(String depTel) {
		dep_tel = depTel;
	}
	public String getDep_fax() {
		return dep_fax;
	}
	public void setDep_fax(String depFax) {
		dep_fax = depFax;
	}
	public String getDep_desc() {
		return dep_desc;
	}
	public void setDep_desc(String depDesc) {
		dep_desc = depDesc;
	}
	public String getDep_updep() {
		return dep_updep;
	}
	public void setDep_updep(String depUpdep) {
		dep_updep = depUpdep;
	}
	public String getDep_date() {
		return dep_date;
	}
	public void setDep_date(String depDate) {
		dep_date = depDate;
	}
    
    
    
}
